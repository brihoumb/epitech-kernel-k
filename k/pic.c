#include "io.h"

void init_pic(void)
{
	outb(0x20, 0x11);
	outb(0xA0, 0x11);
	outb(0x21, 0x20);
	outb(0xA1, 0x28);
	outb(0x21, 0x04);
	outb(0xA1, 0x02);
	outb(0x21, 0x01);
	outb(0xA1, 0x01);
	outb(0x21, 0xfc);
	outb(0xA1, 0xff);
	outb(0x20, 0x20);
	outb(0xA1, 0x20);
	outb(0xA0, 0x3f);
	__asm__("sti\n");
}
